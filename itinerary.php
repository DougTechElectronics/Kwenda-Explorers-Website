        <h3 class="day-title">Day 8 – 11: Nyika National Park in Rumphi</h3>
        <p>Enjoy your last morning and breakfast on the shore of Lake Malawi before you proceed
with the adventure to one of the most beautiful parks in Africa – the Nyika National Park
situated further north of Malawi.
Nyika National Park, in northern Malawi, is country`s oldest and largest National Park
covering an area of 3200 sq Km. The park covers the whole area of the plateau, a
unique upland with most parts lying about 2000 m above sea level offering breath-taking
views, like none other in the whole of Africa. The Park is one of Malawi’s most scenic
and spectacular wilderness areas. It boosts an incredible mixture of scenery and
landscapes – pristine brachystegia woodlands, mature mountain forest patches, crystal
clear running streams and miles and miles of open rolling grasslands.
It is a World heritage site by UNESCO. `Nyika` means where the water comes from. You
will be overwhelmed with the beautiful scenery of the amazing Nyika plateau and
enjoy the montane vegetation which is an attraction to Zebra and Antelope. Other
small mammals like warthogs and bush dogs will also be worth part of your time.
Please keep attention as there are chances* to spot lion in the park, however
leopards are also part of your game. Be aware that this is a home of more than 400
bird species-a perfect place for bird watchers and landscape lovers.</p>
