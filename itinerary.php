        <h3 class="day-title">Day 10-13 Nyika National Park-Chelinda Lodge</h3>
        <p>From Luwawa you will proceed with the adventure to one of the most beautiful parks in Africa – the Nyika National Park situated further north of Malawi. 
Nyika National Park, in northern Malawi, is country`s oldest and largest National Park covering an area of 3200 sq Km. The park covers the whole area of the plateau, a unique upland with most parts lying about 2000 meters above sea level offering breath-taking views, like none other in the whole of Africa. The Park is one of Malawi’s most scenic and spectacular wilderness areas. It boosts an incredible mixture of scenery and landscapes – pristine brachystegia woodlands, mature mountain forest patches, crystal clear running streams and miles and miles of open rolling grasslands.
Nyika national park is a World heritage site by UNESCO. `Nyika` means where the water comes from.  
Accommodation: The good news for you is that you will have a perfect home. The beautiful Chelinda Lodge, located in the highlands of Nyika National Park, will be your home and it offers the guests very spacious accommodation. The lodge lies on the backdrop of the towering pine trees. Situated high on the plateau, the eight luxuriously appointed chalets offer panoramic views over the rolling grasslands of the plateau, where wildlife including large herds of roan, eland and zebra can be spotted around the lodge. The lodge sits on 2300 meters above sea level overlooking the rolling grasslands.
The cabin styled chalets are constructed of stone and pine, and each is fitted with an en-suite bathroom, fireplace, an upstairs seating area and a viewing deck. Meals can be enjoyed in the spacious main lounge or on the grand outdoor deck that overlooks the grasslands. 
Activities: Hiking and trekking in the trails; and game drive will be activities that will overwhelm you with the beautiful scenery of the amazing Nyika plateau and enjoy the montane vegetation which is an attraction to Zebra and Antelope. Other small mammals like warthogs and bush dogs will also be worth part of your time. Please keep attention as there are chances* to spot lion in the park, however leopards are also part of your game. Be aware that this is a home of more than 400 bird species-a perfect place for bird watchers and landscape lovers, therefore birding is an inevitable activity. Fly fishing is possible but seasonal.
</p>
