appointed chalets offer paranomic views over the rolling grasslands of the plateau,
where wildlife including large herds of roan, eland and z ebra can be spotted around the
lodge. The lodge sits on 2300 meters above sea level overlooking the rolling
grasslands.
The cabin styled chalets are constructed of stone and pine, and each is fitted with an
en-suite bathroom, fireplace, an upstairs seating area and a viewing deck. Meals can be
enjoyed in the spacious main lounge or on the grand outdoor deck that overlooks the
grasslands.</p>


        
